    <h2 class="text-center mb-4">Cum să adopti o pisică</h2>
    <p><strong>1. Alegerea unei pisici potrivite</strong></p>
    <p>Pentru a începe procesul de adopție, începeți prin a explora pisicile disponibile în adăpostul nostru. Fiecare pisică are propriile ei caracteristici și personalitate, așa că asigurați-vă că găsiți o pisică care se potrivește cu stilul și nevoile dvs. de viață.</p>
    
    <p><strong>2. Contactați-ne</strong></p>
    <p>După ce ați găsit o pisică care vă interesează, contactați-ne pentru a programa o întâlnire. Puteți folosi formularul nostru online sau puteți suna direct la adăpost pentru a stabili o vizită.</p>
    
    <p><strong>3. Vizitați adăpostul</strong></p>
    <p>Venind la adăpost, veți avea ocazia să întâlniți pisica în persoană și să vă cunoașteți. Unul dintre membrii echipei noastre vă va ghida și vă va oferi mai multe informații despre istoricul și nevoile pisicii.</p>
    
    <p><strong>4. Procesul de adopție</strong></p>
    <p>Dacă sunteți încântat de pisica pe care ați întâlnit-o și sunteți gata să o adăugați în familia dvs., vom începe procesul de adopție. Acest proces implică completarea unor formulare, o verificare a locuinței și, în unele cazuri, o taxă de adopție.</p>
    
    <p><strong>5. Luarea pisicii acasă</strong></p>
    <p>După ce toate formalitățile sunt finalizate, sunteți pregătit să luați pisica acasă! Vom oferi sfaturi și recomandări pentru a face tranziția cât mai ușoară posibilă pentru pisică și pentru dvs.</p>
