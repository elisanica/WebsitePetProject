    <p> Drumul nostru către Rai…sau Iad nu este pavat cu intenții bune, ci cu pisici salvate din stradă și date înspre adopție
Scriu acest text pt ca am primit al mie-lea mesaj in care cineva ne spune ca a “gasit o pisica pe strada dar nu o poate tine” pentru ca mai are inca doua.
Oameni buni, aici nu e ca la jaful unei banci sau ca la o luare de ostatici: semnalarea cazului (catre politie sau in cazul de fata catre o asociatie) nu este suficienta.<br><br>Daca gasesti un animal abandonat in strada, mai ales daca e bland, il iei. Punct. Si vezi apoi ce faci cu el. Solutii se gasesc si suntem primii care vom ajuta oameni dispusi sa ajute, dispusi sa se implice.Poate munca noastra trece neobservata, dar asta nu inseamna ca ea nu exista, si aici ma refer la frecventa cu care postam anunturile de adoptie. Dati o raita pe toate grupurile de animale sau pe paginile asociatiilor si vedeti de cate ori pe zi posteaza ANUNTURI PRELUATE si apoi comparati cu pagina Booking Catspert – Pisici pe creier.<br><br> Cu ajutorul anunturilor postate de noi s-au dat spre adoptie sute de pisici si nu ma refer la cele care au trecut pe la noi prin casa ci la anunturile pe care voi ni le trimiteti si noi le postam. Zi de zi, noapte de noapte. De ce se dau pisicile postate pe pagina noastra? Pentru ca muncim bine, muncim cu cap si cu ajutorul vostru am reusit sa cream o comunitate, nu un grup in care lumea isi etaleaza pisicile si ii intreaba pe ceilalti ce nume ar trebui sa le puna.Sa semnalezi cazul si atat e ca si cum ai gasi un om impuscat pe strada si te oferi sa ii oferi primul ajutor doar daca cineva promite sa il duca la spital.Daca Divinitatea, destinul sau intamplarea iti da ocazia de a face o fapta buna, eu zic sa apucati aceasta ocazie cu ambele maini. Faptele bune nu se fac cu masura, ci pur si simplu…se fac. Pana la capat. Nu sunt aritmetica.<br><br>Nu zici pas la pisica asta pe care tocmai ai gasit-o, pentru ca ai mai salvat doua tot de pe strada. Nu e ca si cum ai bagat o saorma in tine si nu mai incape inca una.Cam asta e treaba cu faptele bune, sunt al dracu de greu de facut! Necesita consum de energie, de nervi, de bani. Iti distrug sanatatea si te fac sa simti ca imbatranesti intr-un an cat altii in 5. Invers decat Fat-Frumos.
Noi stim asta pentru ca…asta facem. Si stim si cat de greu (va) este pentru ca am fost in locul vostru. Nu ne-am nascut cu asociatie. Nu. Am salvat prima pisica de pe strada la vreo 5 luni dupa ce i-am adoptat pe Milito si Zanetti, motanii nostri. Apoi am salvat-o pe a doua, apoi pe a treia, pana cand am ajuns la 130 si ceva…<br><br>Coincidenta face ca fix azi sa fi salvat inca o pisica de pe strada. A fost un caz semnalat de cineva, dar persoana respectiva nu a putut sa prinda pisica, de aceea am “intrat noi in scena”. Ne-am prezentat la fata locului, slava Domnului am prins-o si ne-am dus cu ea la veterinar, pentru ca cineva din zona a spus ca ar fi cazut de pe acorperisul unei case. Paranteza: pisica e bine, la noi. <br>
